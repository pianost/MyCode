/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
	if(__HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE) && __HAL_UART_GET_IT_SOURCE(&huart2, UART_IT_IDLE)) 
	{
		HAL_UART_DMAStop(&huart2);		
		HAL_UART_Receive_DMA(&huart2, RxData, sizeof(RxData));  //数据接收
		memcpy(&Deal_posture, RxData, sizeof(RxData_Typedef));
		static uint8_t posture_count=0;
		switch(posture_count)
		{
			case 0:
				if(Deal_posture.Head1 == 0x0d)	
					posture_count++;
				else
					posture_count=0;
				break;
			case 1:
				if(Deal_posture.Head2==0x0a)
				{
					posture_count+=2;
				}
				else if(Deal_posture.Head2==0x0d);
				else
					posture_count=0;
				break;
			case 3:
				if(Deal_posture.Tail1==0x0a)
					posture_count++;
				else
					posture_count=0;
				break;
			case 4:
				if(Deal_posture.Tail2==0x0d)
				{
					zangle=Deal_posture.posture_data[0];
					xangle=Deal_posture.posture_data[1];
					yangle=Deal_posture.posture_data[2];
					pos_x =Deal_posture.posture_data[3];
					pos_y =Deal_posture.posture_data[4];
					w_z   =Deal_posture.posture_data[5];
				}
				posture_count=0;
				break;
			default:
				posture_count=0;
			break;
		}	
	 }     
#if 0
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
#endif
  /* USER CODE END USART2_IRQn 1 */
}

/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
	if(__HAL_UART_GET_FLAG(&huart3, UART_FLAG_IDLE) && __HAL_UART_GET_IT_SOURCE(&huart3, UART_IT_IDLE)) 
	{
		HAL_UART_DMAStop(&huart3);	
		HAL_UART_Receive_DMA(&huart3, usart3_dma_buff, sizeof(usart3_dma_buff));  //数据接收
		uint8_t LV53_data[35];
		memcpy(&LV53_data, usart3_dma_buff, sizeof(LV53_data));
		if(LV53_data[6] == '0' || LV53_data[6] == '7')
		{
			LV53_distance = (LV53_data[29] - '0');
			if(LV53_data[28] != ' ')
				LV53_distance += (LV53_data[28] - '0')*10;

			if(LV53_data[27] != ' ')
				LV53_distance += (LV53_data[27] - '0')*100;
			if(LV53_data[26] != ' ')
				LV53_distance += (LV53_data[26] - '0')*1000;
		}
	}
	
#if 0
  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */
#endif

  /* USER CODE END USART3_IRQn 1 */
}

